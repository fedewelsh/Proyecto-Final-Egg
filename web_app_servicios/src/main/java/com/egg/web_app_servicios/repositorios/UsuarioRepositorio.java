package com.egg.web_app_servicios.repositorios;

import com.egg.web_app_servicios.entidades.Usuario;
import java.util.List;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

@Repository
public interface UsuarioRepositorio extends JpaRepository<Usuario, String> {
    
    @Query("SELECT n FROM Usuario n WHERE n.nombre = :nombre")
    public List<Usuario> buscarPorNombre(@Param("nombre")String nombre);

}
